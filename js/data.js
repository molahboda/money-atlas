  var s10 = SERIES.us10y, s2 = SERIES.us2y;
  var start = Math.max(s10.start, s2.start);
  var vals = [];
  for (var y = start; y <= YEAR_END; y++) {
    var a = s10.vals[y - s10.start], b = s2.vals[y - s2.start];
    vals.push(Math.round((a - b) * 100) / 100);
  }
