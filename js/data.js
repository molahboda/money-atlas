function eraNameFor(year) {
  var best = null;
  for (var i = 0; i < ERAS.length; i++) {
    var e = ERAS[i];
    if (year >= e.y0 && year <= e.y1) { if (!best || e.y0 >= best.y0) best = e; }
  }
  return best ? best.title : '';
}

